function ploshad(figure, num1, num2) {
    if (typeof Number === figure, num1, num2) {
        continue;
    }
    if (figure === "triangle") {
        console.log ((num1 * num2) / 2);
    } else if (num1 === "circle") {
        console.log ((num1 * num2) * Math.PI);
    } else if (num2 === "square") {
        console.log (num1*num2);
    }
}

ploshad("triangle", 4, 5);
ploshad("circle", 6);
ploshad("square", 4);